<h3>Présentation</h3>
<p>La Hawaï Team a été mandatée afin de créer un nouveau logiciel pour remplacer le logiciel Gala. Le nom du projet devait impérativement commencer par la lettre H. Nous avons choisi de le nommer HILE, qui est le diminutif de HIgh LEvel (en français cela signifie « études supérieures »), pour indiquer que l'application finale sera une évolution de l'actuel logiciel Gala. Comme l'appelation High Level est souvent utilisée dans les pays anglophone pour désigner les études supérieures, elle convient très bien au cursus d'ingénieur que nous suivons.</p>
<p>Hile devra reprendre les fonctionnalités présentes dans Gala, mais aussi prendre en compte les demandes des différents utilisateurs soumises lors des réunions.</p>

