<h1>Accueil</h1>
         <p>La Hawaï Team a été mandatée pour réaliser un nouveau logiciel afin de remplacer l'existant du CFAI Ingénieurs 2000 de Champs sur Marne (77). En effet, l'ancien logiciel ne répondait pas intégralement aux besoins des utilisateurs.
Notre logiciel, nommé HILE, permetera de gérer les relations entre les apprentis, les tuteurs enseignants, les tuteurs ingénieurs, les écoles et le CFAI Ingénieurs 2000.</p>
         <p>Le nom du projet devait impérativement commencer par la lettre H. Nous avons choisi de le nommer HILE, qui est formé à partir des mots HIgh et LEvel. Comme l'appelation High Level est souvent utilisée dans les pays anglophone pour désigner les études supérieures, ce nom convient très bien au domaine dans lequel elle est utilisée.</p>

